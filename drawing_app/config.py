# config.py - アプリ全体で使う定数・設定値
#
# 物理的なプリンター仕様や UI の見た目に関する数値をここに集約しています。
# 数値を変えたいときはこのファイルだけ編集すれば OK です。

import os
import sys
from pathlib import Path
from dotenv import load_dotenv

# .exe として実行中は sys.executable の隣の .env を、
# スクリプト実行中は config.py の隣の .env を読む
if getattr(sys, "frozen", False):
    _base = Path(sys.executable).parent
else:
    _base = Path(__file__).parent

load_dotenv(_base / ".env")

# ---------------------------------------------------------------------------
# プリンター物理定数
# ---------------------------------------------------------------------------

MAX_PRINTER_SIZE_X = 220.0  # プリンタープレートの X 方向の最大サイズ (mm)
MAX_PRINTER_SIZE_Y = 215.0  # プリンタープレートの Y 方向の最大サイズ (mm)
GLOBAL_Y_OFFSET    = 40.0   # 自作ノズルの取り付け位置によるYオフセット (mm)
                             # ノズルが原点から 40mm ずれているため、全座標にこの値を加算する

MATERIAL_SIZE = 5.0         # 飴1個（素材ブロック）の一辺サイズ (mm)
JOINT_SIZE    = 3.0         # ブロック間の目地（隙間）幅 (mm)
CELL_SPACE    = MATERIAL_SIZE + JOINT_SIZE  # セル1つ分のピッチ = 8.0mm
                                             # （素材5mm + 目地3mm）
DESIGN_GAP    = 20.0        # デザイン（1塊）同士の間隔 (mm)
                             # プレート上に複数デザインを並べるときの余白

# ---------------------------------------------------------------------------
# Gコード生成定数
# ---------------------------------------------------------------------------

DRAW_Z  = 2.0   # ノズルが素材に当たる Z 高さ (mm)。低すぎると素材を押しつぶすので注意
SAFE_Z  = 10.0  # 移動時にノズルが素材に引っかからないよう退避する Z 高さ (mm)
F_SPEED = 2250  # 移動速度 (mm/min)。G0/G1 コマンドの F パラメータに使用

# カラー名 → プリンターに送るコマンド文字列のマッピング
# OCTO90X はノズルユニットへの色指定カスタムコマンド
CMD_MAP = {
    "white":  "OCTO901",  # 白（ベース色）
    "pink":   "OCTO902",  # ピンク
    "yellow": "OCTO903",  # 黄色
}

# ---------------------------------------------------------------------------
# UI 設定
# ---------------------------------------------------------------------------

CELL_PIXEL_SIZE   = 150   # UI上で1セルを何ピクセルで表示するか
GRID_SIZE_MIN     = 3    # グリッドの最小マス目数（3×3）
GRID_SIZE_MAX     = 16   # グリッドの最大マス目数（16×16）
GRID_SIZE_DEFAULT = 3    # アプリ起動時のデフォルトマス目数

# 使用できるカラーの一覧（順序付きリスト）
# この順序がUIのボタン並び順にも使われる
COLORS = ["white", "pink", "yellow"]

# ---------------------------------------------------------------------------
# OctoPrint 接続設定
# ---------------------------------------------------------------------------

OCTOPRINT_URL     = "http://3dz5.local"
OCTOPRINT_API_KEY = os.environ.get("OCTOPRINT_API_KEY", "")